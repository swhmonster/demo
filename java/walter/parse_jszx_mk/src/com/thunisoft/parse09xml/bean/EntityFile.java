package com.thunisoft.parse09xml.bean;

/**
 * 待保存的实体文件
 * @author xiegq
 *
 */
public class EntityFile {
    
    // 文件的相对路径，与数据库中保存的一致
    private String path;
    // 文件内容
    private byte[] data;
    
    private Integer wslb;
    
    public EntityFile(String path, byte[] data, Integer wslb) {
        super();
        this.path = path;
        this.data = data;
        this.wslb = wslb;
    }
    public String getPath() {
        return path;
    }
    public byte[] getData() {
        return data;
    }
    
    public Integer getWslb() {
    	return this.wslb;
    }

}
